package Loops.Do_While_loop;

public class DoWhile {
    public static void main(String[] args) {

        int n = 1;
        do
        {
            //when you want to print n natural number 
            // System.out.println(n);
            System.out.println(n + " ");
            ++n;
        }
        while(n<=10);
        // while(n<0);
    }
    
}
