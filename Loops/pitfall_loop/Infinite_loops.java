package Loops.pitfall_loop;

public class Infinite_loops {
    public static void main(String[] args) {
        for (int i =5; i != 0; i -= 2)
        {
            System.out.println(i);
        }
        int x = 5;
        while(x == 5){
            System.out.println("In the loop");
        }
    }
    
}
